package com.project.dto;

public class TotalValues {

    private int totalProduits;
    private int totalCommande;
    private int totalFacture;
    private int totalSales;
    private double totalRevenu;
    private double totalCost;
    private int totalClient;
    private int totalFournisseur;

    public TotalValues() {
    }

    public int getTotalProduits() {
        return totalProduits;
    }

    public void setTotalProduits(int totalProduits) {
        this.totalProduits = totalProduits;
    }

    public int getTotalCommande() {
        return totalCommande;
    }

    public void setTotalCommande(int totalCommande) {
        this.totalCommande = totalCommande;
    }

    public int getTotalFacture() {
        return totalFacture;
    }

    public void setTotalFacture(int totalFacture) {
        this.totalFacture = totalFacture;
    }

    public int getTotalSales() {
        return totalSales;
    }

    public void setTotalSales(int totalSales) {
        this.totalSales = totalSales;
    }

    public double getTotalRevenu() {
        return totalRevenu;
    }

    public void setTotalRevenu(double totalRevenu) {
        this.totalRevenu = totalRevenu;
    }

    public double getTotalCost() {
        return totalCost;
    }

    public void setTotalCost(double totalCost) {
        this.totalCost = totalCost;
    }

    public int getTotalClient() {
        return totalClient;
    }

    public void setTotalClient(int totalClient) {
        this.totalClient = totalClient;
    }

    public int getTotalFournisseur() {
        return totalFournisseur;
    }

    public void setTotalFournisseur(int totalFournisseur) {
        this.totalFournisseur = totalFournisseur;
    }
}
